    private void shopItemBoxAddToCartBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_shopItemBoxAddToCartBtnActionPerformed
        // TODO add your handling code here:     
        DefaultTableModel model =  (DefaultTableModel)this.shopItemsTable.getModel();
        JTable table = this.shopItemsTable;
        String shopItemName =  model.getValueAt(table.getSelectedRow(), 0).toString();
        int shopItemQty = parseInt(model.getValueAt(table.getSelectedRow(), 1).toString()); 
        int userSelectedQty = 0;
        boolean shouldAddToCart;
        try {
            userSelectedQty = parseInt(this.shopItemBoxQtyField.getText());
            shouldAddToCart = true;
        } catch(NumberFormatException n ) {
            JOptionPane.showMessageDialog(null, "Please Enter a positive numeric value");
            shouldAddToCart = false;
        } 
        
        // check for negative number
        if (userSelectedQty < 0) {
            shouldAddToCart = false;
            JOptionPane.showMessageDialog(null, "Please Enter a positive numeric value");
        }
        
        if (shouldAddToCart) {
            if (userSelectedQty > shopItemQty) {
                JOptionPane.showMessageDialog(null, "Sorry only " + shopItemQty + " " + shopItemName + "s remaining");
            } else {
                model =  (DefaultTableModel)this.shopItemsTable.getModel();
                  table = this.shopItemsTable;
                Shop.addToCart(model , table , userSelectedQty , shopItemQty);
                this.shopBuyItemBox.setVisible(false);
                this.shopTableScrollPane.setVisible(true);
                this.shopBuyItemBtn.setVisible(true);
                this.shopSearchBar.setVisible(true);
                this.shopEnterItemLabell.setVisible(true);
                // load shop again since the qty in inventory is updated
                Shop.loadItems(model);
            }
        }
        
    }//GEN-LAST:event_shopItemBoxAddToCartBtnActionPerformed

