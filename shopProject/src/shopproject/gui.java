    private void adminInventoryInfoBoxOkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminInventoryInfoBoxOkBtnActionPerformed
        // TODO add your handling code here:
        JTable table = this.adminInventoryTable;
        DefaultTableModel model = (DefaultTableModel)table.getModel();
        String selectedRowItemName = "";
        int selectedRowItemQty = 0;
        double selectedRowItemPrice = 0;
        if (table.getSelectedRow() != -1) {
            selectedRowItemName = table.getValueAt(table.getSelectedRow(), 0).toString().trim().toLowerCase();
            selectedRowItemQty =  parseInt(table.getValueAt(table.getSelectedRow(), 1).toString());
            selectedRowItemPrice = Double.parseDouble(table.getValueAt(table.getSelectedRow(), 2).toString()); 
        }

        
        if (this.adminInventoryInfoBoxOkBtn.getText().equals("Add Item")) {
            try{
                String name = this.adminInventoryInfoBoxName.getText().trim().toLowerCase();
                String qty = this.adminInventoryInfoBoxQty.getText();
                String price = this.adminInventoryInfoBoxPrice.getText(); 
                if (name.equals("") || qty.equals("") || price.equals("") ) {
                    JOptionPane.showMessageDialog(null, "Please Fill all the required fields");
                } else {
                    int parsed_qty = parseInt(qty);
                    double parsed_price = Double.parseDouble(price);
                    if (parsed_qty <= 0 || parsed_price <=0) {
                        JOptionPane.showMessageDialog(null, "Please Enter a positive numerical value in quantity and price fields");
                    } else {
                        adminInventory.addItem(name , parsed_qty , parsed_price);
                        this.adminInventoryItemInfoBox.setVisible(false);
                        UtilFuncs.loadFromFileIntoATable("database/inventory.txt", model); 
                        this.adminInventoryInfoBoxName.setText("");
                        this.adminInventoryInfoBoxPrice.setText("");
                        this.adminInventoryInfoBoxQty.setText("");
                        this.AdminTabInventoryPanel.setVisible(true);
                    }
                }
            } catch(NumberFormatException  e) {
                JOptionPane.showMessageDialog(null, "Please Enter a positive numerical value in quantity and price fields");
            }
        } else if (this.adminInventoryInfoBoxOkBtn.getText().equals("Update Item Quantity")) {
            try{
                int qty = parseInt(this.adminInventoryInfoBoxQty.getText());
                if (qty <= 0) {
                    JOptionPane.showMessageDialog(null, "Please Enter a positive numerical value in quantity and price fields");
                } else {
                   adminInventory.updateQty(selectedRowItemName , qty);
                   this.adminInventoryItemInfoBox.setVisible(false);
                   UtilFuncs.loadFromFileIntoATable("database/inventory.txt", model); 
                   this.adminInventoryInfoBoxQty.setText("");
                   this.AdminTabInventoryPanel.setVisible(true);
                }
            } catch(NumberFormatException e) {
                JOptionPane.showMessageDialog(null,"Please enter a positive numerical value in quantity field");
            }
        } else {
            try{
                double price = Double.parseDouble(this.adminInventoryInfoBoxPrice.getText());
                if (price <= 0) {
                    JOptionPane.showMessageDialog(null,"Please enter a positive numerical value in quantity field");
                } else {
                     adminInventory.updatePrice(selectedRowItemName , price);
                    this.adminInventoryItemInfoBox.setVisible(false);
                    UtilFuncs.loadFromFileIntoATable("database/inventory.txt", model); 
                    this.adminInventoryInfoBoxPrice.setText("");
                    this.AdminTabInventoryPanel.setVisible(true);
                }
            } catch(NumberFormatException e) {
                JOptionPane.showMessageDialog(null,"Please enter a numerical value in price field");
            }
        }
    }//GEN-LAST:event_adminInventoryInfoBoxOkBtnActionPerformed

